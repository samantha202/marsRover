package marsRover;

public class Plateau {
	
	public int upper_limit_x;
	public int upper_limit_y;
	public int lower_limit_x = 0;
	public int lower_limit_y = 0;
	
	public Plateau(int upper_limit_x, int upper_limit_y) {
		super();
		this.upper_limit_x = upper_limit_x;
		this.upper_limit_y = upper_limit_y;
	}	
}

